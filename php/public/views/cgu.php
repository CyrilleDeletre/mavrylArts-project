    <p>
        Bienvenue sur le site de Mavryl Arts.
        La protection de vos données personnelles est une priorité pour nous.
        Cette politique de confidentialité a pour but de vous informer sur la manière dont nous collectons, utilisons, partageons et protégeons vos informations personnelles.
    </p>
</article>



<article>
    <h3>Collecte des Informations</h3>

    <p>
        Nous pouvons collecter différentes informations lorsque vous utilisez notre site, notamment :

        - Informations personnelles : nom, adresse e-mail, adresse postale, numéro de téléphone, etc.
        - Informations de connexion : adresse IP, type de navigateur, pages visitées, temps passé sur le site, etc.
        - Informations financières : détails de paiement et informations de facturation pour les achats d'œuvres d'art ou de services.
        - Autres informations : toute information que vous nous fournissez volontairement via des formulaires de contact, des commentaires ou des inscriptions à des newsletters.
    </p>
</article>

<article>
    <h3>Utilisation des Informations</h3>

    <p>
        Les informations collectées peuvent être utilisées pour :

        - Fournir, opérer et maintenir notre site
        - Améliorer, personnaliser et étendre notre site
        - Comprendre et analyser comment vous utilisez notre site
        - Traiter vos transactions et gérer vos commandes
        - Communiquer avec vous, y compris pour le service client, pour vous fournir des mises à jour et d'autres informations relatives au site, et à des fins promotionnelles
        - Gérer les concours, promotions, sondages ou autres fonctionnalités du site
        - Protéger contre les fraudes et améliorer la sécurité du site
    </p>
</article>

<article>
    <h3>Partage des Informations</h3>

    <p>
        Nous ne vendons, n'échangeons ni ne louons vos informations personnelles à des tiers. Nous pouvons partager vos informations avec :

        - Prestataires de services : des tiers de confiance qui nous aident à exploiter notre site, à mener nos affaires ou à vous servir, tant que ces parties conviennent de garder ces informations confidentielles.
        - Conformité légale : lorsque cela est requis par la loi ou pour répondre à une procédure légale.
        - Transfert d'entreprise : en cas de fusion, acquisition ou vente de tout ou partie de nos actifs, vos informations personnelles peuvent être transférées au nouveau propriétaire.
    </p>
</article>

<article>
    <h3>Sécurité des Informations</h3>

    <p>Nous mettons en œuvre une variété de mesures de sécurité pour protéger vos informations personnelles. Toutefois, aucune méthode de transmission sur Internet ou de stockage électronique n'est totalement sécurisée,
        et nous ne pouvons garantir une sécurité absolue.</p>
</article>

