    <p>
        Je suis une artiste autodidacte passionnée par la nature, les humains et les animaux. 
        Inspirée par les merveilles du monde naturel et par la diversité de la vie, 
        mon travail artistique est une célébration de cette beauté. 
        Je m'efforce de capturer l'essence même de la vie, 
        des panoramas majestueux aux détails les plus délicats des créatures qui peuplent notre planète, 
        en passant par la complexité des émotions humaines. 
        Chaque œuvre est une ode à la splendeur de la nature et une invitation à la contemplation.</p>
